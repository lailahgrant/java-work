public class Fordemo2 {
    public static void main(String[] args) {
        int sum=0;
        for(int i=0; i<=10; i=i+1)
        {
            sum=sum+i;
        }
        System.out.println("Sum of 1st 10 numbers is "+sum);
    }
}
